// spread operator serve para unir arrays .

let arr1 = [100, 200];

let arr2 = [50 , 75];

let arr3 = [ 10 , 20];

let unirArr = [ ...arr1, ...arr2];   // metodo spread utiliza ... para representar os itens dos arryas 

console.log(unirArr);  // dessa forma os valores de cada array serão exibidos em um unico array .

